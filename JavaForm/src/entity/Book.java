package entity;

/**
 *
 * @author minh
 */
public class Book {
    private String id ;
    private String author;
    private String publishing;
    private int year;
    private float price;
    private String date;

    public Book() {
    }

    public Book(String id, String author, String publishing, int year, float price, String date) {
        this.id = id;
        this.author = author;
        this.publishing = publishing;
        this.year = year;
        this.price = price;
        this.date = date;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getPublishing() {
        return publishing;
    }

    public void setPublishing(String publishing) {
        this.publishing = publishing;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
    
    
    
}
